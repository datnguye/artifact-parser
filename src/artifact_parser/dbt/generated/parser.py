def _parse(artifact: dict, *, expected_prefix: str, kind: str):
    """Resolve ``artifact`` to its model and validate it against that model.

    Raises:
        UnknownArtifactError: if the schema version is unknown, or known but not
            of the family named by ``expected_prefix``.
    """
    schema_version = get_dbt_schema_version(artifact)
    if not schema_version.startswith(expected_prefix):
        raise UnknownArtifactError(f"Not a dbt {kind} artifact: {schema_version}")
    model_class = get_artifact_type(schema_version).value.model_class
    return model_class(**artifact)


def _parse_exact(artifact: dict, artifact_type: ArtifactTypes):
    """Parse ``artifact``, requiring it to be exactly ``artifact_type``.

    Raises:
        UnknownArtifactError: if the artifact's version is not this exact one.
    """
